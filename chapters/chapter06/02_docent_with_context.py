import os
from dotenv import load_dotenv
from anthropic import Anthropic

# .env 파일에서 환경변수를 불러온다.
load_dotenv()

# Anthropic API Key를 환경변수에서 가져온다.
api_key = os.getenv("ANTHROPIC_API_KEY")

if not api_key:
    raise ValueError("ANTHROPIC_API_KEY가 .env 파일에 설정되어 있지 않습니다.")

# Claude 클라이언트 생성
client = Anthropic(api_key=api_key)

# 온라인 공개 자료를 바탕으로 정리한 실습용 앳플리 Context
# 주의: 실제 정책은 변경될 수 있으므로 고객 응대 시 공식몰/앱/고객센터 확인이 필요하다.
atflee_context = """
[앳플리 브랜드/제품 정보]
- 앳플리는 헬스케어 제품과 앱을 운영하는 브랜드다.
- 공식몰 기준 주요 카테고리에는 체중계, 문틀철봉, 바이줌, 근력, 요가, 유산소, 줄자, 인센스 등이 있다.
- 스마트 체중계 카테고리에는 T9, T8, 아이그립 계열 등 여러 체중계 제품이 있다.
- 일부 제품명에는 스마트 인바디/체지방/가정용 전자 체중계 등의 표현이 사용된다.

[앳플리 앱 정보]
- 앳플리 앱은 "내 몸의 스마트한 습관"을 지향하는 건강관리 앱이다.
- 앳플리 스마트 체중계와 연동하여 최대 28가지 체성분 데이터를 분석할 수 있다고 안내되어 있다.
- 최대 10명까지 프로필을 등록해 가족 구성원과 함께 건강 관리를 할 수 있다고 안내되어 있다.
- 기록 요약 대시보드를 통해 측정, 활동 기록, 몸무게 목표 등을 확인할 수 있다.
- 스마트 줄자와 연동하여 주요 신체 부위 및 복부비만율을 측정할 수 있다.
- 스마트 줄넘기와 연동하여 카운트다운 모드, 타이머 모드 등으로 운동 기록을 관리할 수 있다.
- 운동 기록, 식단 기록, 감정 기록, 투두 리스트, 추천 카드, 건강 콘텐츠 기능을 제공한다고 안내되어 있다.
- 앱 권한에는 사진, 카메라, 블루투스, 알림, 건강 데이터 관련 권한이 포함될 수 있다.
- 블루투스 권한은 앳플리 기기 연결에 사용된다.
- 알림 권한은 몸무게 측정 알림, 문의 답변, 업데이트 소식 등에 사용될 수 있다.

[배송 정보]
- 공식몰 이용안내 기준 배송 방법은 택배다.
- 배송 지역은 전국 지역으로 안내되어 있다.
- 배송비는 주문 금액과 상관없이 무료로 안내되어 있다.
- 배송 기간은 1일~7일로 안내되어 있다.
- 실제 배송 상태, 송장번호, 지연 사유는 주문별 확인이 필요하다.

[환불 정보]
- 공식몰 이용안내 기준 환불은 반품 확인 여부를 확인한 후 영업일 3일 이내 결제 금액을 환불한다고 안내되어 있다.
- 신용카드 결제의 경우 카드 승인을 취소하여 결제 대금이 청구되지 않게 하는 방식으로 안내되어 있다.
- 단, 카드사 청구일과 환급 일정에 따라 실제 처리 시점은 달라질 수 있다.
- 교환/반품의 세부 조건은 상황별 확인이 필요하다.

[문의 정보]
- 앱 관련 문의 또는 제안은 app@atflee.com 또는 앱 내 1:1 문의로 안내되어 있다.
- 공개된 대표번호는 02-3445-7877이다.
- 앱 안내 기준 운영시간은 평일 10시~17시, 점심시간 12시~13시로 안내되어 있다.
- 실제 AS 접수 여부, 주문 상태, 배송 상태는 고객 정보 확인이 필요하다.

[응대 주의사항]
- 실제 주문 상태, 배송 상태, AS 접수 상태는 절대 추측하지 않는다.
- 제품 고장 여부를 단정하지 않는다.
- 고객이 말하지 않은 사실을 확정하지 않는다.
- 정책은 변경될 수 있으므로 중요한 정책 판단은 공식몰, 앱 1:1 문의, 고객센터 확인이 필요하다고 안내한다.
"""

# Chapter 4.3 원칙을 반영한 시스템 프롬프트
system_prompt = """
# Role
너는 앳플리 제품/앱/정책 도슨트봇이다.

# Goal
사용자의 질문에 대해 제공된 앳플리 Context를 바탕으로 쉽고 친절하게 설명한다.

# Context
사용자는 앳플리 제품, 앱 연결, 배송, 환불, AS, 고객센터 문의 방법에 대해 질문할 수 있다.
너는 반드시 <atflee_context> 안의 정보를 우선 근거로 사용한다.

# Rules
- <atflee_context>에 있는 정보만 확정적으로 말한다.
- <atflee_context>에 없는 정보는 추측하지 않는다.
- 실제 주문 상태, 배송 상태, AS 접수 상태를 지어내지 않는다.
- 정책, 보증, 교환/환불 조건은 확실하지 않으면 "정확한 확인이 필요합니다"라고 말한다.
- 고객이 바로 해볼 수 있는 행동을 안내한다.
- 답변은 초보자도 이해할 수 있게 쉽게 작성한다.
- 너무 길게 답하지 말고 핵심 위주로 작성한다.

# Process
1. 질문이 제품, 앱, 배송, 환불, AS, 문의 중 어디에 해당하는지 분류한다.
2. <atflee_context>에서 관련 내용을 찾는다.
3. 확실한 정보와 확인이 필요한 정보를 구분한다.
4. 사용자가 바로 할 수 있는 다음 행동을 제안한다.

# Output Format
아래 형식으로 답변한다.

1. 간단한 답변
2. 근거가 되는 앳플리 정보
3. 바로 해볼 수 있는 것
4. 확인이 필요한 것
"""

# 실습용 사용자 질문 — 이 변수를 바꿔서 다양한 질문을 테스트할 수 있다.
user_question = """
앳플리 체중계가 앱이랑 연결이 안 될 때 어떻게 해야 하나요?
배송이나 AS 문의는 어디로 하면 되나요?
"""

# XML 태그로 Context와 사용자 질문을 분리한다.
# Claude는 XML 구조를 잘 이해하므로 두 내용을 명확히 구분할 수 있다.
prompt = f"""
<atflee_context>
{atflee_context}
</atflee_context>

<user_question>
{user_question}
</user_question>
"""

# Claude API 호출
# temperature=0.2: Context 기반 답변이므로 창의성을 더 낮추고 정확성을 높인다.
response = client.messages.create(
    model="claude-sonnet-4-5",
    max_tokens=1200,
    temperature=0.2,
    system=system_prompt,
    messages=[
        {
            "role": "user",
            "content": prompt
        }
    ]
)

# Claude 답변 출력 (Windows cp949 환경에서 이모지 등 특수문자 인코딩 오류 방지)
import sys
sys.stdout.reconfigure(encoding="utf-8", errors="replace")
print(response.content[0].text)
